# En este ejercicio tendréis que crear un módulo que contenga las operaciones básicas de una calculadora: sumar, restar, multiplicar y dividir.
def Suma(num1, num2):
    return num1 + num2

def Resta(num1, num2):
    return num1 - num2

def Multiplicar(num1, num2):
    return num1 * num2

def Division(num1, num2):
    return num1 / num2